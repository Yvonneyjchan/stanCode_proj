import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    dummy = ListNode()
    carry = 0  # 存被十進位的數字
    cur = dummy

    while l1 or l2:
        if l1:
            v1 = l1.val
        else:
            v1 = 0  # 如果v1 沒有值, 還是要存0去給加總(V2)

        if l2:
            v2 = l2.val
        else:
            v2 = 0

        sum_ = v1 + v2 + carry

        carry = sum_ // 10
        sum_ = sum_ % 10
        cur.next = ListNode(sum_)

        cur = cur.next  # cur往前
        if l1:
            l1 = l1.next  # l1往前
        # else:
        #     l1 = None
        if l2:
            l2 = l2.next  # l2往前
        # else:
        #     l2 = None

    if carry:  # 如果carry還有值, 要進到最前面一位數
        cur.next = ListNode(carry)

    return dummy.next


def traversal(head):
    """
    :param head: ListNode, the first node to a linked list
    -------------------------------------------
    This function prints out the linked list starting with head
    """
    cur = head
    while cur.next is not None:
        print(cur.val, end='->')
        cur = cur.next
    print(cur.val)


def main():
    args = sys.argv[1:]
    if not args:
        print('Error: Please type"python3 add2.py test1"')
    else:
        if args[0] == 'test1':
            l1 = ListNode(2, None)
            l1.next = ListNode(4, None)
            l1.next.next = ListNode(3, None)
            l2 = ListNode(5, None)
            l2.next = ListNode(6, None)
            l2.next.next = ListNode(4, None)
            ans = add_2_numbers(l1, l2)
            print('---------test1---------')
            print('l1: ', end='')
            traversal(l1)
            print('l2: ', end='')
            traversal(l2)
            print('ans: ', end='')
            traversal(ans)
            print('-----------------------')
